public class Ordenador {

    private String modeloCPU;
    private int tamDISC;
    private int tamRAM;
    private double precio;

    public String getModeloCPU() {
        return modeloCPU;
    }

    public void setModeloCPU(String modeloCPU) {
        this.modeloCPU = modeloCPU;
    }

    public int getTamDISC() {
        return tamDISC;
    }

    public void setTamDISC(int tamDISC) {
        this.tamDISC = tamDISC;
    }

    public int getTamRAM() {
        return tamRAM;
    }

    public void setTamRAM(int tamRAM) {
        this.tamRAM = tamRAM;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
}
